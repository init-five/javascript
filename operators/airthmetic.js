let x = 10;
let y = 3;

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);
console.log(x ** y); // x to the power of y

// increment ++
console.log(++x); // 11
console.log(x++); //10
console.log(x); //11

// decrement --
console.log(--x); // 9
console.log(x--); //10
console.log(x); //9
